     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $stock = stock::findOrFail($id);
        if($stock) {
            if($this->validate($request,[
                'stock' => ['required','string'],
            ])){
                $stock->stock = $request->stock;
                $stock->save();
                return redirect()->back();
            }
            return redirect()->back();
        }
        return redirect()->back();
    }

